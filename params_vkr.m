%% params_vkr.m
% Параметры модели символьной синхронизации (Глава 2 ВКР)
% Платформа: MATLAB R2025b + Communications Toolbox
% Тема: Разработка устройства символьной синхронизации цифрового
%       приёмопередатчика на базе SDR (Zynq-7000 + AD9361)

clear; clc;

%% ====== Этап 2.1. Параметры передатчика ======
M       = 4;                    % порядок модуляции (QPSK)
k_bits  = log2(M);              % бит на символ (= 2)
Rs      = 1e6;                  % символьная скорость, симв/с
Ts      = 1/Rs;                 % длительность символа, с
Rb      = Rs * k_bits;          % битовая скорость, бит/с (= 2 Мбит/с)

sps_tx  = 8;                    % отсчётов на символ в TX (имитация "аналога")
Fs_tx   = Rs * sps_tx;          % частота дискретизации TX, Гц (= 8 МГц)

rolloff = 0.35;                 % коэффициент скругления RRC
span    = 10;                   % длина RRC-фильтра в символах

Nsym    = 10000;                % длина пакета в символах

%% ====== Этап 2.2. Параметры канала ======
timing_offset = 0.3;            % дробное смещение тактов в долях символа Ts
                                % 0.3*Ts = 2.4 отсчёта АЦП при sps_tx=8
sco_ppm       = 0;              % расхождение тактовых частот, ppm (резерв)
EbNo_dB       = 30;             % отношение Eb/N0 для одиночного прогона, дБ

%% ====== Этап 2.3. Параметры приёмника ======
sps_rx = 2;                     % отсчётов на символ на входе петли Gardner
decim  = sps_tx / sps_rx;       % фактор децимации в RRC-приёмнике (= 4)
Fs_rx  = Rs * sps_rx;           % частота дискретизации после децимации (= 2 МГц)

%% ====== Этап 2.4-2.5. Параметры петли символьной синхронизации ======
BnT  = 0.02;                    % нормированная шумовая полоса петли (Rice)
zeta = 1/sqrt(2);               % коэффициент затухания (~0.707, критическое)

%% --- Расчёт коэффициентов PI-фильтра по методике Rice ---
K0 = -1;                        % коэффициент усиления NCO (вычитающий счётчик mod-1)
Kp =  2.7;                      % коэффициент усиления Gardner TED для RRC alpha=0.35

% Промежуточная нормированная частота
theta_n = (BnT) / (zeta + 1/(4*zeta));

% Произведения K0*Kp*K1 и K0*Kp*K2 по формулам Rice
K0Kp_K1 = (4*zeta*theta_n) / (1 + 2*zeta*theta_n + theta_n^2);
K0Kp_K2 = (4*theta_n^2)    / (1 + 2*zeta*theta_n + theta_n^2);

% Сами коэффициенты PI-фильтра
K1 = K0Kp_K1 / (K0*Kp);
K2 = K0Kp_K2 / (K0*Kp);

%% ====== Вывод сводки ======
fprintf('=================================================\n');
fprintf('  Параметры модели ВКР загружены в workspace\n');
fprintf('=================================================\n');
fprintf('Передатчик:\n');
fprintf('  Модуляция         : QPSK (M=%d, k=%d бит/символ)\n', M, k_bits);
fprintf('  Символьная скорость Rs   = %.3f МГц\n', Rs/1e6);
fprintf('  Битовая скорость    Rb   = %.3f Мбит/с\n', Rb/1e6);
fprintf('  Передискретизация TX     = %d sps\n', sps_tx);
fprintf('  Частота дискретизации TX = %.3f МГц\n', Fs_tx/1e6);
fprintf('  RRC: alpha=%.2f, span=%d символов\n', rolloff, span);
fprintf('  Длина пакета             = %d символов\n', Nsym);

fprintf('\nКанал:\n');
fprintf('  Timing offset      = %.3f * Ts (= %.2f отсчётов АЦП)\n', ...
        timing_offset, timing_offset*sps_tx);
fprintf('  SCO                = %d ppm\n', sco_ppm);
fprintf('  Eb/N0              = %.1f дБ\n', EbNo_dB);

fprintf('\nПриёмник:\n');
fprintf('  Передискретизация RX     = %d sps\n', sps_rx);
fprintf('  Фактор децимации в RRC   = %d\n', decim);
fprintf('  Частота на входе петли   = %.3f МГц\n', Fs_rx/1e6);

fprintf('\nПетля символьной синхронизации:\n');
fprintf('  BnT      = %.4f\n', BnT);
fprintf('  zeta     = %.4f\n', zeta);
fprintf('  theta_n  = %.6f\n', theta_n);
fprintf('  K0*Kp*K1 = %.6f\n', K0Kp_K1);
fprintf('  K0*Kp*K2 = %.6f\n', K0Kp_K2);
fprintf('  K1       = %.6e\n', K1);
fprintf('  K2       = %.6e\n', K2);
fprintf('=================================================\n');